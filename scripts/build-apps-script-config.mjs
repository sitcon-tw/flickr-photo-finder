import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname } from "node:path";

const outputPath = "apps-script/GeneratedConfig.js";

function fieldForAppsScript(field) {
  return {
    name: field.name,
    labelZh: field.label_zh ?? field.name,
    type: field.type,
    required: Boolean(field.required),
    multiValue: Boolean(field.multi_value),
    taxonomyKey: field.taxonomy_key ?? "",
    descriptionZh: field.description_zh ?? "",
  };
}

async function readJson(path) {
  return JSON.parse(await readFile(path, "utf8"));
}

async function main() {
  const schema = await readJson("data/photo-schema.json");
  const taxonomy = await readJson("data/tag-taxonomy.json");
  const photosTable = schema.tables.photos;

  const config = {
    generatedAt: "not recorded in committed source",
    schemaVersion: schema.version,
    photosSheetName: "photos",
    schemaMetaSheetName: "schema_meta",
    headers: photosTable.fields.map((field) => field.name),
    fields: photosTable.fields.map(fieldForAppsScript),
    reviewedRequiredFields: photosTable.reviewed_required_fields ?? [],
    approvedRequiredFields: photosTable.approved_required_fields ?? [],
    taxonomy,
  };

  const content = `// Generated by scripts/build-apps-script-config.mjs. Do not edit manually.
var SITCON_PHOTO_FINDER_CONFIG = ${JSON.stringify(config, null, 2)};
`;

  await mkdir(dirname(outputPath), { recursive: true });
  await writeFile(outputPath, content);
  console.log(`Apps Script config written to ${outputPath}`);
}

try {
  await main();
} catch (error) {
  console.error(`Could not build Apps Script config: ${error.message}`);
  process.exitCode = 1;
}
